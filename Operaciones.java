
public class Operaciones {

	int resultado;
	
	public int sumar(int num1, int num2) {
		resultado=num1+num2;
		return resultado;
	}
	public int restar(int num1, int num2) {
		resultado=num1-num2;
		return resultado;
	}
	public int multiplicar(int num1, int num2) {
		resultado=num1*num2;
		return resultado;
	}
	public int dividir(int num1, int num2) {
		resultado=num1/num2;
		return resultado;
	}
	
}
